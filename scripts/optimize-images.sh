const imageminWebp = require('imagemin-webp');
const imageminPngquant = require('imagemin-pngquant');
const imageminMozjpeg = require('imagemin-mozjpeg');

(async () => {
    // Process PNG and JPG files
    const files = await imagemin(['public/images/*.{jpg,jpeg,png}'], {
        destination: 'public/images/optimized',
        plugins: [
            imageminMozjpeg({quality: 85}),
            imageminPngquant({
                quality: [0.65, 0.8]
            })
        ]
    });

    console.log('ğŸ“¸ Optimized images:', files.map(f => f.destinationPath));

    // Generate WebP versions
    const webpFiles = await imagemin(['public/images/*.{jpg,jpeg,png}'], {
        destination: 'public/images/optimized',
        plugins: [
            imageminWebp({quality: 85})
        ]
    });

    console.log('ğŸŒ Generated WebP images:', webpFiles.map(f => f.destinationPath));
})();
EOF

